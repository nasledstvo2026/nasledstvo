ws2 = wb.create_sheet("Меры для детей участников СВО")

headers2 = [
    "№", "Название меры", "Категория участника СВО (родителя)",
    "Категория ребёнка", "Размер меры", "Периодичность",
    "Условия предоставления", "Где оформить", "Список документов",
    "Как получить", "Тип меры", "Категория льготы",
    "Уровень", "Нормативный акт-основание"
]

for col_idx, header in enumerate(headers2, 1):
    ws2.cell(row=1, column=col_idx, value=header)

style_header(ws2, [get_column_letter(i) for i in range(1, len(headers2)+1)])

children_measures = [
    (m["name"], m["category"], m["amount"], m["period"], m["conditions"],
     m["where"], m["docs"], m["how"], m["type"], m["cat"], m["level"], m["npa"])
    for m in measures
    if "дети" in m["name"].lower() or "Дети" in m["name"]
]

# Add also measures that relate to children even if not in name
child_keywords = ["ребён", "школ", "детск", "школьн", "студент", "колледж"]
for m in measures:
    name_lower = m["name"].lower()
    cat_lower = m["category"].lower()
    if any(kw in name_lower for kw in child_keywords) or any(kw in cat_lower for kw in child_keywords):
        if m["name"] not in [c[0] for c in children_measures]:
            children_measures.append(
                (m["name"], m["category"], m["amount"], m["period"], m["conditions"],
                 m["where"], m["docs"], m["how"], m["type"], m["cat"], m["level"], m["npa"])
            )

# Deduplicate
seen = set()
unique_children = []
for cm in children_measures:
    if cm[0] not in seen:
        seen.add(cm[0])
        unique_children.append(cm)

for row_idx, cm in enumerate(unique_children, 2):
    # Extract parent category
    parent_cat = cm[1]
    child_cat = "Дети участников СВО"
    if "школьн" in cm[0].lower():
        child_cat = "Дети-школьники (1-11 классы)"
    elif "колледж" in cm[0].lower() or "студент" in cm[0].lower():
        child_cat = "Студенты колледжей"
    elif "детск" in cm[0].lower():
        child_cat = "Дети дошкольного возраста"
    elif "вуз" in cm[0].lower():
        child_cat = "Студенты вузов"

    ws2.cell(row=row_idx, column=1, value=row_idx - 1)
    ws2.cell(row=row_idx, column=2, value=cm[0])
    ws2.cell(row=row_idx, column=3, value=parent_cat)
    ws2.cell(row=row_idx, column=4, value=child_cat)
    ws2.cell(row=row_idx, column=5, value=cm[2])
    ws2.cell(row=row_idx, column=6, value=cm[3])
    ws2.cell(row=row_idx, column=7, value=cm[4])
    ws2.cell(row=row_idx, column=8, value=cm[5])
    ws2.cell(row=row_idx, column=9, value=cm[6])
    ws2.cell(row=row_idx, column=10, value=cm[7])
    ws2.cell(row=row_idx, column=11, value=cm[8])
    ws2.cell(row=row_idx, column=12, value=cm[9])
    ws2.cell(row=row_idx, column=13, value=cm[10])
    ws2.cell(row=row_idx, column=14, value=cm[11])

style_data(ws2, len(unique_children)+1, len(headers2))

col_widths_2 = [5, 55, 30, 30, 28, 18, 45, 35, 35, 35, 18, 25, 22, 40]
for i, w in enumerate(col_widths_2, 1):
    ws2.column_dimensions[get_column_letter(i)].width = w

