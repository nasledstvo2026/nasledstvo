# Определяем уровень для каждой меры
def get_level(name, law):
    federal_keywords = [
        "ФЗ №181-ФЗ", "ФЗ №166-ФЗ", "ФЗ №400-ФЗ", "ФЗ №178-ФЗ", 
        "ФЗ №273-ФЗ", "НК РФ", "ТК РФ", "Указ Президента", 
        "ФЗ №40-ФЗ", "ФЗ №442-ФЗ"
    ]
    regional_keywords = [
        "Закон г. Москвы", "Постановление Правительства Москвы", 
        "Распоряжение Правительства Москвы", "Ведомственные акты ДСЗН",
        "Регламенты mos.ru", "Закон г. Москвы №70"
    ]
    for kw in regional_keywords:
        if kw in law:
            return "Региональная (московская)"
    for kw in federal_keywords:
        if kw in law:
            return "Федеральная (с московскими особенностями)"
    # Check if law mentions both
    has_fed = any(kw in law for kw in federal_keywords)
    has_reg = any(kw in law for kw in regional_keywords)
    if has_fed and has_reg:
        return "Федеральная + Региональная"
    return "Не указано"

